class Item {
  final String id;
  final String title;
  final String manufacturer;
  final double price;
  final int quantity;
  final String imageUrl;
  final String description;

  Item({
    required this.id,
    required this.title,
    required this.manufacturer,
    required this.price,
    required this.quantity,
    required this.imageUrl,
    required this.description,
  });
}
